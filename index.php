<h3>Women Challenge Tech's 'Bro Culture’</h3>

<p class="byline">Story By: <a href="https://www.linkedin.com/in/samanthapaisley/">Samantha Paisley</a></p>
<p class="byline">Interactives By: <a href="http://madisonwalls.org/">Madison Walls</a></p>


<p>Juhi Patel knows her stuff. Her academic performance in computer science at UNC-Chapel Hill is stellar. She's excelled in web development internships.</p>

<p>Now, as she awaits her interview with a software development firm, she's confident. And nervous. </p>

<p>She’s the only female applicant in the room. Her name is called, and three men begin interviewing her. </p>

<p>They scribble notes as she talks, avoiding all eye contact. They ask nothing about her background or interests. They interrupt her answers. Then, after she was told there would be no technical questions, they begin peppering her with questions about advanced programming. </p>

<p>Her confidence shaken, she holds back tears. She could tell they weren’t interested in her as soon as she walked in. </p>

<p>She texts her male friend who interviewed for the same position. He tells her no technical questions were asked during his interview. </p>

<p> “That just further proved it for me — the reason why they were asking me these technical questions and drilling me, the three of them continuously, one after the other, was because I was a female,” she said. “Those are the interviews that just destroy your mentality and hopes.” </p>

<p>Patel's experience isn't unique. Stories about gender discrimination in the tech industry are common. But tech organizations led by women are changing that. </p>

<p>***</p>

<p>There are more than 600,000 unfilled tech jobs in the United States, and that number is expected to double by 2020. Tech salaries are double the national average salary. Such promising prospects draw many sharp minds to the field. </p>

<p>In entry-level college computer science classes, 40 to 50 percent of students are women, but only 18 percent of graduating classes are female, according to Rewriting the Code, a non-profit based in the Raleigh-Durham Triangle that combats college dropout rates for women in tech. </p>

<p> “It seems almost criminal that someone could be as passionate as they are about technology, computer science (or) engineering, and something happens at that college level where they just start to lose confidence and have self-doubt,” said Sue Harnett, co-founder of the non-profit. </p>

<h3>Gendered problems at every stage of life</h3>

<p>Harnett said women are socialized from youth to believe they are ill-suited for careers in science and math and are re-directed towards the humanities. This socialization often hinders access to opportunities. </p>

<p> “The stereotype of the ‘white male genius’ founder perpetuates investment in white men versus minority men and women of all races and ethnicities,” said Elizabeth Ames, senior vice president of Market, Alliances and Programs at AnitaB.org. </p>

<p>The lack of female role models in tech is another subtle yet powerful factor that discourages young women from pursuing these careers. Facebook’s Sheryl Sandberg summarizes this problem: “You can’t be what you can’t see.” </p>

<p>Women who prevail against these stereotypes and study computer science in college report ubiquitous isolation. </p>

<p> “When I realized how male-dominated it was, and how there was only a couple of girls in each class, I felt like this wasn't where I was supposed to be,” she said. </p>

<img src="img/cs-3.jpg" alt="UNC Computer Science" width="80%" height="auto">

<p>Computer science classes not only are mostly populated by male students; they are also mostly taught by men. Diane Pozefsky, director of Undergraduate Studies for Computer Science at UNC-Chapel Hill, said just 17 percent of the pool of those with PhD’s in computer science are female.</p>

<p>Even when women enter professional technical spaces, isolation and belittlement are common, women we talked to said.</p>

<p>Lisa Smith, co-director of the Women Who Code chapter in Raleigh-Durham, was called “office mom” because she was the only woman in the office.</p>

<p> “I think it was meant as a term of endearment, but it also pigeonholed me into a place where I was seen as everyone's mom and not taken seriously,” she said. “Sometimes you don't recognize it until you're out of a situation, but there have been plenty of places where I was meant to feel less just because of the fact that I was a woman.” </p>

