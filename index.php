<h3>Outside organizations come to the rescue</h3>

<p>But there are organizations tackling these problems by supporting girls from middle school all of the way to mid-career professional women. </p>

<p>TechGirlz is a national nonprofit dedicated to inspiring middle school girls to pursue technical careers. Tracey Welson-Rossman founded the organization in 2010 <p>after reading reports that girls were self-selecting themselves out of tech careers around ninth grade. </p>

<p>Her organization produces free curricula totaling 50 lesson plans for companies and community groups to train girls across the nation. To retain girls at the front of the tech career pipeline, she created techshopz — hands-on workshops that focus on engaging projects. </p>

<p>By March, TechGirlz will have reached 10,000 girls nationwide. One set of volunteers is located in the Research Triangle Park area, where recent sessions focused on electronic circuits and JavaScript. </p>

<p> “What we see is that girls want to solve a problem,” Welson-Rossman said. “So if we create the materials in such a way that they're solving a problem, and they're using tech to solve that problem, it makes it more interesting for them. It brings it more to life, which is really important.” </p>

<h3>***</h3>

<p>Harnett co-founded Rewriting the Code to bring together communities of college women passionate about technology. The program supports, trains and mentors women in programming to connect them with technical internships and opportunities. Applications for Rewriting the Code’s program skyrocketed from 150 800 in a single year.</p>

<p> “To its very core, to see women who are so incredibly brilliant in their fields and passionate about what they do become so discouraged that they quit was something that I just couldn't let it go,” Harnett said. “When you see the women have an opportunity to have validation that they're not the only ones feeling this way, it becomes very empowering.” </p>

<h3>***</h3>

<p>Hack-a-thons also give college students an opportunity to hone their programming skills and connect with mentors in the region. But most hack-a-thons are male-dominated and occur over weekends. Many young women aren’t comfortable staying overnight at remote locations with unfamiliar male students, so many opt out. </p>

<p>To address this concern, Maegan Clawges founded Pearl Hacks, an all-female hack-a-thon. </p>

<p> “My whole intention behind Pearl Hacks was that it should be an event where women felt comfortable,” she said. “You're not going to get the same people at this hack-a-thon that you would get at others because this is removing the intimidation.” </p>

<p>Keren Tseytlin participated in the first year of Pearl Hacks. Though she had only taken a couple of computer science classes, she won the Capitol One competition and landed her first engineering job.</p>

<p> “Pearl Hacks 2015 changed my entire career direction — there's no way that I (otherwise) would have the first job that I have today,” she said. “It was really cool that you could take workshops from these women who were rocking it and be surrounded by a classroom that doesn't look like your everyday computer science classroom.” </p>

<p>She returned the next year to Pearl Hacks as a mentor and co-leader, illustrating the positive cycle of female mentorship that can emerge. </p>

<h3>***</h3>

<p>Since 41 percent of women leave tech careers after a decade, organizational support must continue beyond the entry-point of the pipeline. Women Who Code sprung into action in 2011 to support career-aged tech professionals and has since grown to over 100,000 members. </p>

<p>In April 2017, a Women Who Code network was launched in the Raleigh-Durham area. In the span of eight months, membership has grown to nearly 500 women. </p>

<p>Lisa Smith is one the branch’s two directors. She said Women Who Code fills a void because little support and uplift occurs for women who are mid-career. Prior to Women Who Code, Smith knew very few professional women in tech. </p>

<p>"While I was standing in front of everyone (during the first meeting), I realized with both extreme pleasure and also some sadness that I didn't know any of the women who were in front of me," she said. “My job is to try to keep women there — not only for their own career advancement, but selfishly so I have colleagues and people that I can continue to work with.” </p>

<p>She plans outreach events and workshops to train women for perfecting technical interviews, polishing resumes and approaching salary negotiation talks. Monthly brunch sessions are among the most popular events — they are spaces for women to bond over shared experiences and share advancement opportunities. </p>

<p> “With our current political climate and many other issues, safe spaces are hard to come by,” Smith said. “I've learned more in those brunch sessions I think that the last five years of just floating around in the community without any anchor like that." </p>

<h3>Moving forward</h3>

<p>Though organizations provide support for girls and women in tech, changes in education and workplace culture would alleviate isolation and hardship.

<p>Dona Sarkar, head of the Windows Insider Program at Microsoft, said computer science classes must be mandatory, akin to math in a school’s curriculum, which would naturally balance entry numbers and foster more inclusivity. </p>

<p>But true diversity surpasses physical appearance. Sarkar emphasizes difference in perspective, background and history to build a genuinely diverse company. </p>

<p> “I actually find gender diversity and racial diversity to be lazy — it doesn’t mean anything at a deeper level,” she said. “A black person from Stanford who grew up in Paolo Alto does not have the same perspective as someone who grew up in inner-city Detroit or someone from Lagos, Nigeria.” </p>

<p>She’s confident diversity in the workplace will happen organically because society is in the midst of resetting acceptable standards of behavior. Norms created by and for white men are rapidly changing. </p>

<p>Patel is ready for that change. Tech’s problem with diversity fuels her determination to land a competitive software job out of college. </p>

<p> “What gender you are, that shouldn’t matter,” she said. “It's just motivated me more to work harder, apply to more jobs, do better at these interviews and make sure that these companies know who I am.” </p>


